// class to create characters
public class Characters {
    String characterName;
    String characterDialogue;
// method to set character names
    public void setCharacterName(String characterNameChoice){
        this.characterName = characterNameChoice;
    }
// method to get character names
    public String getCharacterName(){
        return this.characterName;
    }
// method to set character names
    public void setCharacterDialogue(String characterDialogueChoice){
        this.characterDialogue = characterDialogueChoice;
    }
// method to get character names
    public String getCharacterDialogue(){
        return this.characterDialogue;
    }
}